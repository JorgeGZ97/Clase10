    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // TODO add your handling code here:
        Connection con = conect.getConnection();
        String sql = "Insert into usuarios1(fecha,usuario,clave) values( ?,  ?,  ?)";
        try {
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, ((JTextField) Jdate.getDateEditor().getUiComponent()).getText());
            pst.setString(2, txtusuario.getText());
            pst.setString(3, txtpassword.getText());
            int n = pst.executeUpdate();
            if (n>0) {
               JOptionPane.showMessageDialog(null, "Los datos se ingresaron satisfactoriamente en la base de datos");
            }else{
                JOptionPane.showMessageDialog(null, "Los datos no se ingresaron en la base de datos");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al ingresar registro" + e.getMessage());
        }
    }//GEN-LAST:event_btnagregarActionPerformed

