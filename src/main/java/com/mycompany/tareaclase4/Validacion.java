    public void validaracceso() {
        int resultado = 0;
        try {
            String Usuario= txtusuario.getText();
            String Pass = String.valueOf(txtpassword.getPassword());
            String sql = "select * from usuarios1 where usuario='"+Usuario+"' and clave='"+Pass+"'";
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            if (rs.next()) {
                resultado=1;
                if (resultado==1) {
                    Sistema form= new Sistema();
                    form.setVisible(true);
                    this.dispose();
                }else {
                   JOptionPane.showMessageDialog(null, " Revise los datos porque contiene error");
                }
            }
        }catch (Exception e) {
          JOptionPane.showMessageDialog(null, " Su error es "+e.getMessage());  
        }
    }
    
